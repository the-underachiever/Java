public class prog6_EscapeSequence {
    public static void main(String[] args) {

        // ***Escape Sequence Characters***
        System.out.println("My name is \"Achyut\" ");
        System.out.println("\\");
        /*
        
        to print back slash (\) use \\
        to print double quote (") use \"
        to print text on new line use \n
        to print tab (extra space) use \t

         */
    }
}
